    //method to populate the staff sales table
    protected static void populateProductTable(ArrayList<Products> products){
        for(int index = 0; index < products.size(); index++){
            
            int productID = products.get(index).getProductID();
            String barcode = products.get(index).getBarcode();
            String category = products.get(index).getCategory();
            String name = products.get(index).getName();
            double costPrice = products.get(index).getCostPrice();
            int quantity = products.get(index).getQuantity();
            Date purchaseDate = products.get(index).getDate();
            String description = products.get(index).getDescription();
            String purchasedFrom = products.get(index).getPurchasedFrom();
            String image = products.get(index).getImage();
            
            insertIntoAdminProductTable(productID +"", barcode, category, name, costPrice +"", quantity +"", purchaseDate.toString(), description, purchasedFrom);
        }
    }
    
    //populate staff sales table
    protected static void insertIntoAdminProductTable(String productID, String barcode, String category, String name, String costPrice, String quantity, String purchaseDate, String description, String purchasedFrom){
        String[] rowData = {productID, barcode, category, name, costPrice, quantity, purchaseDate, description, purchasedFrom};
        dtm.addRow(rowData);
    }
    
    //get details of selected product from table
    public Products getSelectedProductDetail(){
            int productID = Integer.parseInt(adminProductTable.getValueAt(adminProductTable.getSelectedRow(), 0).toString());
            String barcode = adminProductTable.getValueAt(adminProductTable.getSelectedRow(), 1).toString();
            String category = adminProductTable.getValueAt(adminProductTable.getSelectedRow(), 2).toString();
            String name = adminProductTable.getValueAt(adminProductTable.getSelectedRow(), 3).toString();
            double costPrice = Double.parseDouble(adminProductTable.getValueAt(adminProductTable.getSelectedRow(), 4).toString());
            int quantity = Integer.parseInt(adminProductTable.getValueAt(adminProductTable.getSelectedRow(), 5).toString());
            Date purchaseDate = Date.valueOf(adminProductTable.getValueAt(adminProductTable.getSelectedRow(), 6).toString());
            String description = adminProductTable.getValueAt(adminProductTable.getSelectedRow(), 7).toString();
            String purchasedFrom = adminProductTable.getValueAt(adminProductTable.getSelectedRow(), 8).toString();
            String image = dbConnection.getProductImagePath(productID);

            Products products = new Products(productID, barcode, category, name, costPrice, quantity, purchaseDate, description, purchasedFrom, image);
            
            return  products;
    }

