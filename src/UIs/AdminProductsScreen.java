    private void removeSelectedProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_removeSelectedProductMouseClicked
        if(adminProductTable.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(this, "No product selected.", "message", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            int deleteOption = JOptionPane.showConfirmDialog(this, "Do You Really Want To Delete This Product", "Delete Product", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            
            if(deleteOption == JOptionPane.YES_OPTION){
                int productID = Integer.parseInt(adminProductTable.getValueAt(adminProductTable.getSelectedRow(), 0).toString());
                dbConnection.deleteProduct(productID);
                
                dtm.setRowCount(0);
                products = dbConnection.getAllProducts();
                populateProductTable(products);
                JOptionPane.showMessageDialog(this, "Product Successfully Deleted.", "Message",JOptionPane.INFORMATION_MESSAGE);
            }
        }
        
    }//GEN-LAST:event_removeSelectedProductMouseClicked

