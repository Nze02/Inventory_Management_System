    public void updateAddedCategory(){
        String CategoryName = categoryNameField.getText();

        Category updatedCategory = new Category(CategoryName);
        int insertStatus = dbConnection.insertCategory(updatedCategory);
        //set table contents with updated values
        if(insertStatus > 0){
            CategoryScreen.dtm.setRowCount(0);
            ArrayList<Category> categoryList = dbConnection.getProductCategories();
            CategoryScreen.populateCategoryTable(categoryList);
            JOptionPane.showMessageDialog(this, "Category Successfully added.", "Message",JOptionPane.INFORMATION_MESSAGE);
        }
        
    }

