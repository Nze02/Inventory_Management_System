    private void removeCustomerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeCustomerButtonActionPerformed
        if(customerDetailsTable.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(this, "No customer selected.", "message", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            int deleteOption = JOptionPane.showConfirmDialog(this, "Do You Really Want To Delete This Customer", "Delete Customer", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            
            if(deleteOption == JOptionPane.YES_OPTION){
                int CustomerID = Integer.parseInt(customerDetailsTable.getValueAt(customerDetailsTable.getSelectedRow(), 0).toString());
                dbConnection.deleteCustomer(CustomerID);
                
                dtm.setRowCount(0);
                customers = dbConnection.getAllCustomersDetails();
                populateCustomerTable(customers);
                clearDisplayLabels();   //clear fields 
                JOptionPane.showMessageDialog(this, "Customer Successfully Deleted.", "Message",JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_removeCustomerButtonActionPerformed

