    //method to populate the customer table
    protected static void populateCustomerTable(ArrayList<Customer> customers) {
        for (int index = 0; index < customers.size(); index++) {

            int customerID = customers.get(index).getCustomerID();
            String firstName = customers.get(index).getCustomerFirstName();
            String lastname = customers.get(index).getCustomerLastName();
            String phone = customers.get(index).getCustomerPhone();
            String email = customers.get(index).getCustomerEmail();
            double totalPurchase = customers.get(index).getTotalPurchase();

            insertIntoCustomerTable(customerID, firstName, lastname, phone, email, totalPurchase);
        }
    }

    //populate customer table
    protected static void insertIntoCustomerTable(int customerID, String customerFirstName, String customerLastName, String customerPhone, String customerEmail, double totalPurchase) {
        String[] rowData = {customerID + "", customerFirstName, customerLastName, customerPhone, customerEmail, totalPurchase + ""};
        dtm.addRow(rowData);
    }

